def convert_to_base(n, base): #Функция для перевода числа в систему счисления с основанием base.

    # Строка, содержащая все возможные цифры для систем счисления с основаниями до 36
    digits = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ"
    
    # Если число меньше основания, возвращаем соответствующую цифру
    if n < base:
        return digits[n]
    else:
        # Рекурсивный вызов функции для получения следующей цифры
        # и добавление текущей цифры (остаток от деления на основание)
        return convert_to_base(n // base, base) + digits[n % base]

# Пример использования функции
number = int(input("Введите число для перевода: "))
base = int(input("Введите основание системы счисления: "))
result = convert_to_base(number, base)
print(f"Число {number} в системе счисления с основанием {base} равно {result}")
